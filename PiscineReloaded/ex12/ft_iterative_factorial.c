int	ft_iterative_factorial(int nb)
{
	int fac;
	int i;

	if (nb < 0)
		return (0);
	else
	{
		fac = 1;
		i = 1;
		while (i <= nb)
		{
			fac *= i;
			i++;
		}
		return (fac);
	}
}
