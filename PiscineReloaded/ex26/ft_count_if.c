int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	success;

	i = 0;
	success = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
			success++;
		i++;
	}
	return (success);
}
