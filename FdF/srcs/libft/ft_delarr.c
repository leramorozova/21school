#include "libft.h"

void		ft_delarr(char ***arr)
{
	int		t;
	char	**str;

	t = 0;
	str = *arr;
	while (*str)
	{
		ft_strdel(str);
		t++;
		str = &(*arr)[t];
	}
	ft_memdel((void **)arr);
}
