#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0' && (int)s[i] != c)
		i++;
	if (s[i] == '\0' && c != '\0')
		return (NULL);
	else
		return ((char *)&(s[i]));
}
