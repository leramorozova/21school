#include "libft.h"

int		ft_delarr(char ***arr)
{
	int		t;

	t = 0;
	while ((*arr)[t])
	{
		ft_strdel(&((*arr)[t]));
		t++;
	}
	if (*arr)
		free(**arr);
	arr = NULL;
	return (0);
}
