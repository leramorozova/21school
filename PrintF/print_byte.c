#include "ft_printf.h"

void		print_byte(int a)
{
	int		i;

	i = 8;
	while (--i != -1)
	{
		if (a & (1 << i))
			write(1, "1", 1);
		else
			write(1, "0", 1);
	}
	write(1, "\n", 1);
}
