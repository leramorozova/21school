#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == 2147483647)
	{
		ft_putstr("2147483647");
		return ;
	}
	else if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		n *= -1;
		ft_putchar('-');
	}
	if (n < 10)
	{
		ft_putchar(n + '0');
		return ;
	}
	ft_putnbr(n / 10);
	ft_putchar((n % 10) + '0');
	return ;
}
