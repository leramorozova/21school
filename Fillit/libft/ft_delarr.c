#include "libft.h"

int		ft_delarr(char ***arr, int t)
{
	if (arr[t])
		free((*arr)[t--]);
	while (t != -1)
		free((*arr)[t--]);
	free(*arr);
	*arr = NULL;
	return (0);
}
